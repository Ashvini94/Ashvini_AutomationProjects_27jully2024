package constructor;

/* constructor --> it is executed whenever an object is created
 * 
 * whenever object is created bydefault automatically constructor will be executed internally that is called as default constructor
 * 	
 * rules to define constructor -->
 * 1.constructor name should be same as class name
 * 2.it has not return type (not even void)
 * 3.it can not use with non-access modifiers like abstract,final,static,symchronized
 * 4.it can use with access modifiers like public,protected,default,private
 * 
 * 
 */

public class Constructor_001 {

	//constructor
	public Constructor_001() //constructor can take all the access modifiers i.e public,private,protected,default
	{
	System.out.println();
	}
	
	
	public static void main(String[] args) {



	}

}
