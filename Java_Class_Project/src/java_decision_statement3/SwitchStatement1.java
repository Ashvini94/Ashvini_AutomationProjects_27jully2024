package java_decision_statement3;

public class SwitchStatement1 {

	public static void main(String[] args) {


		String s="geography";
		
		//switch statement
		switch(s)
		{
		case "science":
			System.out.println("Value is science");
			break;
		case "math":
			System.out.println("Value is math");
			break;
		case "physics":
			System.out.println("value is physics");
			break;
		default:
			System.out.println("All values are invalid");
			break;
		
		}
		
		
		

	}

}
