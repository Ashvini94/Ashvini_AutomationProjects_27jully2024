package loop_statement;

public class For_Loop {

	public static void main(String[] args) {

		/*syntax -->
		 * 
		 * 	for(vraiable name;condition;inc/dec)
		 * 	{	
		 * 		//block of code
		 * 	}
		 */
		

		//for loop
		
		for(int i=0;i<10;i++)
		{
			System.out.println("value of i is: "+i);
		}
		
		//for loop
		for(int j=10;j>0;j--)
		{
			System.out.println("Value of j is: "+j);
		}

	}

}
