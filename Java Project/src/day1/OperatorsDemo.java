package day1;

public class OperatorsDemo {

	public static void main(String[] args) {
		
		//Swapping of two numbers using arithmatic operators 
		
		
		int a,b;
		a=10;
		b=20;
		
		//for swapping of two numbers 
		b=20-a; //20-10=10
		System.out.println(b);//10
		
		a=10+a; //10+10=20
		System.out.println(a);//20
		
	}

}
